function comparaNome(a, b) {
    if (normalize(a.nome) < normalize(b.nome)) {
      return -1;
    }
    if (normalize(a.nome) > normalize(b.nome)) {
      return 1;
    }
    // a deve ser igual a b
    return 0;
}

